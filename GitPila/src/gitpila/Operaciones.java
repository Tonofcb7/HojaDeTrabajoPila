public class Operaciones {
    
    public int Suma (int x, int y){
        int suma;
        suma= x + y;
        return suma;
    }
    
    public int Resta (int x, int y){
        int resta;
        resta= x-y;
        return resta;
    }
    
    public int Division (int x, int y){
        int division;
        division= x/y;
        return division;
    }
    
    public int Multiplicacion(int x, int y){
        int mul;
        mul= x*y;
        return mul;
    }
}
